package com.sdjzu.basicinformationservice.entity;

public class ApiParamRelation {
    private String paramno;

    private String paramname;

    private String type;

    private String apiin;

    private String next;

    private String apiout;

    public String getParamno() {
        return paramno;
    }

    public void setParamno(String paramno) {
        this.paramno = paramno;
    }

    public String getParamname() {
        return paramname;
    }

    public void setParamname(String paramname) {
        this.paramname = paramname;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getApiin() {
        return apiin;
    }

    public void setApiin(String apiin) {
        this.apiin = apiin;
    }

    public String getNext() {
        return next;
    }

    public void setNext(String next) {
        this.next = next;
    }

    public String getApiout() {
        return apiout;
    }

    public void setApiout(String apiout) {
        this.apiout = apiout;
    }
}